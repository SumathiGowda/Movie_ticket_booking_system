    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    String language = languageField.getText();
String location = locationField.getText();
String date = dateField.getText();
String time = timeField.getText();
String cost = costField.getText();

String updateQuery = "UPDATE movies_table SET language=?, location=?, date=?, time=?, price=? WHERE movie_id=? AND username=?";
String deleteQuery = "DELETE FROM tickets_table WHERE movie_id = ?";

int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to update movie ID " + movie_id + "?", "Confirm Update", JOptionPane.YES_NO_OPTION);
if (confirm != JOptionPane.YES_OPTION) return;

try (Connection conn = DBConnection.getConnection();
     PreparedStatement updateStmt = conn.prepareStatement(updateQuery);
     PreparedStatement deleteStmt = conn.prepareStatement(deleteQuery)) {

    System.out.println("Updating movie with ID: " + movie_id + " and username: " + username);

    // Update
    updateStmt.setString(1, language);
    updateStmt.setString(2, location);
    updateStmt.setString(3, date);
    updateStmt.setString(4, time);
    updateStmt.setString(5, cost);
    updateStmt.setInt(6, movie_id);
    updateStmt.setString(7, username);

    int updated = updateStmt.executeUpdate();
    System.out.println("Movie update count: " + updated);

    // Delete
    deleteStmt.setInt(1, movie_id);
    int deleted = deleteStmt.executeUpdate();
    System.out.println("Tickets deleted count: " + deleted);

    if (updated > 0) {
        JOptionPane.showMessageDialog(this, "Movie updated successfully.");
    } else {
        JOptionPane.showMessageDialog(this, "Failed to update movie.");
    }

    if (deleted > 0) {
        JOptionPane.showMessageDialog(this, "Associated tickets deleted successfully.");
    } else {
        JOptionPane.showMessageDialog(this, "No associated tickets found for this movie.");
    }

} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
}


    }//GEN-LAST:event_jButton5ActionPerformed

