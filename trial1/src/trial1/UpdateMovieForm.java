      private void loadMovieFromDatabase(int movie_id, String username) {
          
              
    String query = "SELECT * FROM movies_table WHERE movie_id=? AND username=?";

    try (Connection conn = DBConnection.getConnection();
         PreparedStatement pst = conn.prepareStatement(query)) {

        pst.setInt(1, movie_id);           // Set movie_id to the first placeholder
        pst.setString(2, username);        // Set username to the second placeholder

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {  // Move to the first row
            String title = rs.getString("title");
            titleField.setText(title);
            String genre = rs.getString("genre");
            genreField.setText(genre);
            String duration = rs.getString("duration");
            durationField.setText(duration);
            languageField.setText(rs.getString("language"));
            locationField.setText(rs.getString("location"));
            dateField.setText(rs.getDate("date").toString());
            timeField.setText(rs.getString("time"));
            costField.setText(rs.getBigDecimal("price").toString());
            
        } else {
            titleField.setText("Movie not found");

        }

        titleField.setEditable(false);
        genreField.setEditable(false);
        durationField.setEditable(false);
               languageField.setEditable(true);
        locationField.setEditable(true);
        dateField.setEditable(true);
        timeField.setEditable(true);
        costField.setEditable(true);


    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }

      }


